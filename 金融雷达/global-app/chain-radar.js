      return `<span class="cr-co ${cls}" data-co="${esc(name)}" title="点击看「${esc(name)}」实时情报">${cqTag}<b>${esc(name)}</b><span class="cr-co-q">${price ? price + " " : ""}<em>${chg}</em></span></span>`;
