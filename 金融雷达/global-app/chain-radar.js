  // ===== 赛道 / 企业情报面板：点击弹出，实时搜索东财资讯 =====
  const fmtTime = (t) => {
    const m = String(t || "").match(/(\d{2})-(\d{2})\s+(\d{2}:\d{2})/);
    return m ? `${m[1]}-${m[2]} ${m[3]}` : (t || "");
  };
  let intelSeq = 0;
  function closeIntel() {
    el("cr-intel").hidden = true;
    el("cr-intel-backdrop").hidden = true;
    document.body.classList.remove("cr-intel-open");
  }
  async function openIntel(keyword, locText, cos) {
    const seq = ++intelSeq;
    el("cr-intel-loc").textContent = locText;
    el("cr-intel-title").textContent = keyword;
    const cosHtml = cos && cos.length
      ? `<div class="cr-intel-sec">成分企业</div><div class="cr-intel-cos">${cos.map(coChip).join("")}</div>` : "";
    el("cr-intel-body").innerHTML = cosHtml +
      `<div class="cr-intel-sec">实时情报 · 东财资讯</div><div class="cr-intel-news" id="cr-intel-news"><div class="cr-intel-loading">正在搜索「${esc(keyword)}」实时快讯…</div></div>`;
    el("cr-intel-backdrop").hidden = false;
    el("cr-intel").hidden = false;
    el("cr-intel").scrollTop = 0;
    document.body.classList.add("cr-intel-open");
    try {
      const d = await fetchJSON("/api/cn-search?kw=" + encodeURIComponent(keyword), 10000);
      if (seq !== intelSeq) return; // 已切换到别的赛道，丢弃过期结果
      const news = el("cr-intel-news");
      if (d && d.items && d.items.length) {
        news.innerHTML = d.items.map((n) =>
          `<a class="cr-intel-item" href="${esc(n.url)}" target="_blank" rel="noreferrer">
            <span class="cr-intel-it-title">${esc(n.title)}</span>
            <span class="cr-intel-it-meta"><i>${esc(n.source)}</i> ${esc(fmtTime(n.time))}</span>
          </a>`).join("");
      } else {
        news.innerHTML = `<div class="cr-intel-empty">暂无「${esc(keyword)}」相关实时快讯</div>`;
      }
    } catch (error) {
      if (seq === intelSeq) el("cr-intel-news").innerHTML = `<div class="cr-intel-empty">情报加载失败，请重试</div>`;
    }
  }
  el("cr-main").addEventListener("click", (e) => {
    const co = e.target.closest(".cr-co");
    if (co && co.dataset.co) {
      const chain = chainById(activeChain);
      let found = null;
      chain.layers.forEach((L) => L.groups.forEach((g) => g.cos.forEach((c) => { if (c[0] === co.dataset.co) found = c; })));
      openIntel(co.dataset.co, chain.name + " · 企业情报", found ? [found] : []);
      return;
    }
    const grp = e.target.closest(".cr-group");
    if (grp && grp.dataset.sector) {
      const chain = chainById(activeChain);
      let cos = [];
      chain.layers.forEach((L) => L.groups.forEach((g) => { if (g.sector === grp.dataset.sector && L.tier === grp.dataset.tier) cos = g.cos; }));
      openIntel(grp.dataset.sector, `${chain.name} · ${grp.dataset.tier}`, cos);
    }
  });
  el("cr-intel-close").addEventListener("click", closeIntel);
  el("cr-intel-backdrop").addEventListener("click", closeIntel);
