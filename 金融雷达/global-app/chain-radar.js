  async function fetchJSON(url, timeoutMs = 12000) {
    const ctrl = new AbortController();
    const timer = window.setTimeout(() => ctrl.abort(), timeoutMs);
    try {
      const r = await fetch(url, { cache: "no-store", signal: ctrl.signal });
      if (!r.ok) throw new Error("HTTP " + r.status);
      return await r.json();
    } finally {
      window.clearTimeout(timer);
    }
  }

  function setLive(ok, stale) {
    const node = el("cr-mode");
    const st = el("cr-state");
    if (!node || !st) return;
    if (ok && !stale) { node.textContent = "实时数据"; st.dataset.mode = "live"; }
    else if (ok && stale) { node.textContent = "数据延迟"; st.dataset.mode = "stale"; }
    else { node.textContent = "行情源暂不可用"; st.dataset.mode = "demo"; }
    el("cr-updated").textContent = new Intl.DateTimeFormat("zh-CN", { timeZone: "Asia/Shanghai", hour: "2-digit", minute: "2-digit", second: "2-digit", hour12: false }).format(new Date());
  }

  let syncing = false;
  async function syncQuotes() {
    if (syncing) return;
    syncing = true;
    const chain = chainById(activeChain);
    const secids = listedCodes(chain).map(secidOf).join(",");
    try {
      const d = await fetchJSON("/api/cn-stocks?secids=" + secids);
      if (d && d.ok && d.quotes) {
        quotes = { ...quotes, ...d.quotes };
        renderMain();
        setLive(true, d.stale);
      } else {
        setLive(false);
      }
    } catch (error) {
      setLive(false);
    } finally {
      syncing = false;
    }
  }

